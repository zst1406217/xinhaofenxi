clf;
% fs=100;
fs=32000;
Ndata=16;
N=16;
n=0:Ndata-1;
t=n/fs;
x=cos(2*pi*6.5*1000*t)+cos(2*pi*7*1000*t)+cos(2*pi*9*1000*t);
y=fft(x,N);
mag=abs(y);
f=(0:N-1)*fs/N;
subplot(2,2,1);plot(f(1:N/2),mag(1:N/2)*2/N);
xlabel('频率/Hz');
ylabel('振幅');
title('真实长度16，fft长度30');
grid;

